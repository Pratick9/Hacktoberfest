package com.abhishek.Array;

public class Basic_array {
    public static void main(String[] args) {
        // Syntax
        // datatype [] variable name = new datatype [size];
        int [] marks = {5,5,8,5};
        for (int i = 0; i < marks.length; i++) {
            System.out.println(marks[i]);
        }
    }
}
